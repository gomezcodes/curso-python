def sumar(op1,op2):
    print("El resultado de la suma es: {}".format(op1+op2))

def restar(op1,op2):
    print("El resultado de la resta es: {}".format(op1-op2))

def multiplicar(op1,op2):
    print("El resultado de la multiplicacion es: {}".format(op1*op2))

def dividir(dividendo,divisor):
    print("El resultado de la division es: {}".format(dividendo/divisor))

def potencia(base,exponente):
    print("El resultado de la potencia es: {}".format(base**exponente))

def redondear(numero):
    print("El resultado de la redondeada es: {}".format(round(numero)))